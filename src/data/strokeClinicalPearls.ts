  content: string; // Short preview (existing)
  type: 'pearl' | 'trial' | 'guideline';
  section?: string;
  link?: string;
  evidence?: string;

  // 2026 AHA/ASA alignment:
  evidenceClass?: 'I' | 'IIa' | 'IIb' | 'III';  // AHA recommendation class
  evidenceLevel?: 'A' | 'B' | 'C';                // AHA evidence level
